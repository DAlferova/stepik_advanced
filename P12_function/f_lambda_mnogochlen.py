from functools import reduce


def evaluate(coefficients: list[int], x: int):
    count = len(coefficients)
    coefficients_with_x = map(lambda number, i: number * x ** (count - i - 1), coefficients, range(count))
    result = reduce(lambda num1, num2: num1 + num2, coefficients_with_x, 0)
    return result


elements = [int(num) for num in input().split()]
value = int(input())
print(evaluate(elements, value))


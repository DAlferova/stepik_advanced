"""
Восход
На спутнике «Восход» установлен прибор для измерения солнечной активности. Каждую минуту он передаёт в
обсерваторию по каналу связи положительное целое число — количество энергии солнечного излучения.
Для правильного анализа результатов нет необходимости держать повторяющиеся данные. Напишите программу,
которая выводит максимальное количество показаний спутника, при удалении которых результат будет правильно
проанализирован.

Формат входных данных
На вход программе подаётся одна строка, содержащая числа – показания спутника «Восход». Числа указываются
через пробел и не содержат ведущих нулей.

Формат выходных данных
Программа должна вывести максимальное количество показаний, после удаления которых анализ результатов будет
произведен верно.

Sample Input 1:
10 20 30 10 40
Sample Output 1:
1
"""
mylist = [int(item) for item in input().split()]
count = len(mylist) - len(set(mylist))
print(count)

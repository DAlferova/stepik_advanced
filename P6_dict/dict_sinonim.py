"""
Словарь синонимов
Вам дан словарь, состоящий из пар слов-синонимов. Повторяющихся слов в словаре нет. Напишите программу,
которая для одного данного слова определяет его синоним.

Формат входных данных
На вход программе подается количество пар синонимов nn. Далее следует nn строк, каждая строка содержит
два слова-синонима. После этого следует одно слово, синоним которого надо найти.
Формат выходных данных
Программа должна вывести одно слово, синоним введенного.

Примечание 1. Гарантируется, что синоним введенного слова существует в словаре.
Примечание 2. Все слова в словаре начинаются с заглавной буквы.
for _ in range(int(input())):
    a, b = input().split()
    words[a], words[b] = b, a
"""
n = int(input())
dict_sinonim = {}
for _ in range(n):
    words = input().split()
    dict_sinonim[words[0]] = words[1]
    dict_sinonim[words[1]] = words[0]

print(dict_sinonim[input()])

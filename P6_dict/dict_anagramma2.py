def create_dict_count_of_symbols(text: str) -> dict:
    result = {}
    for s in text:
        result[s] = result.get(s, 0) + 1
    return result


def remove_symbols_from_string(text: str, symbols: str) -> str:
    for symbol in symbols:
        text = text.replace(symbol, '')
    return text



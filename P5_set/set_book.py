"""
Ученики 1010 класса онлайн-школы BEEGEEK получили задание прочесть на летних каникулах три книги:

"Что такое математика?";
"Математическая составляющая";
"100 гениальных идей по математике".
Оказалось, что nn учеников прочитали первую книгу, mm учеников — вторую, kk учеников — третью. Т
акже известно, что xx учеников прочли первую или вторую, или обе эти книги, yy учеников —
вторую или третью, или обе, zz учеников — первую и третью, или хотя бы одну из этих двух книг.
Полностью выполнили задание только tt учеников. Всего в 1010 классе учится aa учеников. Напишите
программу, которая выводит сколько учеников:

прочитали только одну книгу;
прочитали две книги;
не прочитали ни одной из рекомендованных книг.
Формат входных данных
На вход программе подаются числа n, m, k, x, y, z, t, an,m,k,x,y,z,t,a, каждое на отдельной строке.

Формат выходных данных
Программа должна вывести три числа в соответствии с условием задачи, каждое на отдельной строке.
Sample Input:

19
18
22
32
33
35
2
50
Sample Output:

29
12
7
"""
n, m, k, x, y, z, t, a = (int(input()) for _ in range(8))
two_books = 2 * n + 2 * m + 2 * k - x - y - z - 3 * t
one_book = n + m + k - 2 * two_books - 3 * t
no_book = a - one_book - two_books - t
print(one_book)
print(two_books)
print(no_book)

